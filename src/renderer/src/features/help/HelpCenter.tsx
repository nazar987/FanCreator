let openExternal: (() => void) | null = null
/** Открыть справочный центр (из меню кнопки «?»). */
export function openHelpCenter(): void {
  openExternal?.()
}

export function HelpCenter(): React.JSX.Element | null {
  const [open, setOpen] = React.useState(false)
  const [topicId, setTopicId] = React.useState(TOPICS[0].id)
  const [query, setQuery] = React.useState('')

  React.useEffect(() => {
    openExternal = () => {
      setQuery('')
      setOpen(true)
    }
    return () => {
      openExternal = null
    }
  }, [])

  React.useEffect(() => {
    if (!open) return
    const onKey = (event: KeyboardEvent): void => {
      if (event.key === 'Escape') setOpen(false)
    }
    window.addEventListener('keydown', onKey)
    return () => window.removeEventListener('keydown', onKey)
  }, [open])

  if (!open) return null

  const normalized = query.trim().toLowerCase()
  // мягкий поиск: каждое слово запроса ищем как подстроку, длинные слова — ещё и
  // с отрезанным окончанием («рядом» находит «ряд», «таблицы» — «таблица»)
  const terms = normalized.split(/\s+/).filter(Boolean)
  const visible = terms.length
    ? TOPICS.filter((topic) => {
        const hay = (topic.title + ' ' + topic.keywords).toLowerCase()
        return terms.every(
          (term) => hay.includes(term) || (term.length > 4 && hay.includes(term.slice(0, -2)))
        )
      })
    : TOPICS
  const current = visible.find((topic) => topic.id === topicId) ?? visible[0]

  return (
    <div className="modal-overlay" onMouseDown={() => setOpen(false)}>
      <div className="modal help-center" onMouseDown={(event) => event.stopPropagation()}>
        <button className="help-center-close" onClick={() => setOpen(false)} title="Закрыть">
          <X size={16} />
        </button>
        <aside className="help-center-nav">
          <div className="help-center-search">
            <Search size={14} />
            <input
              autoFocus
              value={query}
              placeholder="Найти в справке…"
              onChange={(event) => setQuery(event.target.value)}
            />
          </div>
          <div className="help-center-topics">
            {visible.length === 0 && <div className="help-center-none">Ничего не нашлось</div>}
            {visible.map((topic) => (
              <button
                key={topic.id}
                className={current?.id === topic.id ? 'is-active' : ''}
                onClick={() => setTopicId(topic.id)}
              >
                {topic.icon}
                <span>{topic.title}</span>
              </button>
            ))}
          </div>
        </aside>
        <section className="help-center-body">
          {current ? (
            <>
              <h3>{current.title}</h3>
              {current.body}
            </>
          ) : (
            <p className="dim">Попробуйте другой запрос.</p>
          )}
        </section>
      </div>
    </div>
  )
}
